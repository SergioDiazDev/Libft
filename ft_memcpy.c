#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, t_size_t n)
{
	t_size_t		i;
	const char		*s;
	char			*d;

	s = (const char *)src;
	d = (char *)dest;
	i = 0;
	if (!d && !s)
		return (NULL);
	while (i < n)
	{
		d[i] = s[i];
		i++;
	}
	return (dest);
}
