#include "libft.h"

int	ft_memcmp(const void *str1, const void *str2, t_size_t n)
{
	t_size_t	i;
	char		*s1;
	char		*s2;

	s1 = (char *)str1;
	s2 = (char *)str2;
	i = 0;
	if (n == 0)
		return (0);
	while (s1[i] == s2[i] && i < n - 1)
		i++;
	return ((unsigned char)s1[i] - (unsigned char)s2[i]);
}
